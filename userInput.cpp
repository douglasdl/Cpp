#include <iostream>
using namespace std;

int main() {
  int x; 
  // insertion operator (<<)
  cout << "Type a number: "; // Type a number and press enter
  // extraction operator (>>)
  cin >> x; // Get user input from the keyboard
  cout << "Your number is: " << x << "\n"; // Display the input value
  

  return 0;
}